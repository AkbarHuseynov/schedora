// ── Engine ────────────────────────────────────────────────────────────────────

const LANG_KEY = 'schedora_lang';

function getLang() {
  return localStorage.getItem(LANG_KEY) || 'en';
}

function setLang(lang) {
  localStorage.setItem(LANG_KEY, lang);
  applyLang(lang);
  updateSelector(lang);
}

function t(key) {
  const lang = getLang();
  return (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) ||
         (TRANSLATIONS['en'] && TRANSLATIONS['en'][key]) ||
         key;
}

function applyLang(lang) {
  document.documentElement.lang = lang;

  // Text nodes
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    var key = el.getAttribute('data-i18n');
    var val = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) ||
              (TRANSLATIONS['en'] && TRANSLATIONS['en'][key]) || key;
    el.textContent = val;
  });

  // Placeholder attributes
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-placeholder');
    var val = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) ||
              (TRANSLATIONS['en'] && TRANSLATIONS['en'][key]) || key;
    el.placeholder = val;
  });

  // data-confirm attributes (cancel confirmation dialogs)
  document.querySelectorAll('[data-i18n-confirm]').forEach(function(el) {
    var key = el.getAttribute('data-i18n-confirm');
    var val = (TRANSLATIONS[lang] && TRANSLATIONS[lang][key]) ||
              (TRANSLATIONS['en'] && TRANSLATIONS['en'][key]) || key;
    el.setAttribute('data-confirm', val);
  });
}

function updateSelector(lang) {
  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });
}

document.addEventListener('DOMContentLoaded', function() {
  var lang = getLang();
  applyLang(lang);
  updateSelector(lang);

  document.querySelectorAll('.lang-btn').forEach(function(btn) {
    btn.addEventListener('click', function() {
      setLang(this.dataset.lang);
    });
  });
});
